#Calculate the BMI of any person

'''Pseudo code
1. input the name of the person
2. input the weight of the person
3. input the height of the person
4. BMI = weight/(height*height)
5. Display the BMI for the person'''

print("Lets calculate your BMI")
name = input("Enter your name")
weight = float(input("Enter your Weight"))
height = float(input("Enter your Height"))

BMI = weight/(height*height)

print ("%s Body Mass Index is " %name, BMI)


